package javaexercises.practice.conditions;

public class Exercise58 {
    public static void main(String[] args) {

        // Impar ou Par | Even or Odd

        int number = 2;

        if (number % 2 == 0){
            System.out.println(number + " Even");
        }else {
            System.out.println(number + " Odd");
        }
    }
}
