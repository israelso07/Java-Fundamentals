package javaexercises.practice.operators;

public class Exercise11 {
    public static void main(String[] args) {

        int a = 18;
        int b = 9;

        int resultSum = a + b;
        int resultSubtraction = a - b;
        int resultMultiplication = a * b;
        int resultDivision = a / b;
        int resultRemainder = a % b;

        System.out.println("Result of Sum: " + resultSum);
        System.out.println("Result of Subtration: " + resultSubtraction);
        System.out.println("Result of Multiplication: " + resultMultiplication);
        System.out.println("Result of Division: " + resultDivision);
        System.out.println("Result of Remainder: " + resultRemainder);

    }
}
