package javaexercises.practice.operators;

public class Exercise23 {
    public static void main(String[] args) {

        int stock = 120;

        stock += 35;
        System.out.println("Before add 35 products: " + stock);
        stock -= 42;
        System.out.println("Before sell 42 products: " + stock);
        stock += 18;
        System.out.println("Before add 18 products: " + stock);
    }
}
