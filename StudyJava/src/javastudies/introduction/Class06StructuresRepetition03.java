package javastudies.introduction;

public class Class06StructuresRepetition03 {
    public static void main(String[] args) {

        // Impress the firsts 25 numbers of 50

        int valueMax = 50;
        for (int i = 0; i <= valueMax; i++) {
            if (i > 25) { break;
            }
            System.out.println(i);
        }
    }
}
