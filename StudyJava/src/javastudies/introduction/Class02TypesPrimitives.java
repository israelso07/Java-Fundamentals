package javastudies.introduction;

public class Class02TypesPrimitives {
    public static void main(String[] args) {
        // int, double, float, char, byte, short, long, boolean, String
        int age = 10;
        double salaryDouble = 2000.7;
        float salaryFloat = 2500.0F;
        long bigNumber = 8198725;
        byte ageByte = 18;
        short ageShort = 10;
        String name = "Israel";
        boolean verdadeiro = true;
        boolean falso = false;
        char character = 'M';

        System.out.println("he's age is " + age + " years old");
        System.out.println("my salary is " + salaryDouble);
        System.out.println("my pretension of salary is " + salaryFloat);
        System.out.println("my number is " + bigNumber);
        System.out.println("my age is " + ageByte);
        System.out.println("she's age is " + ageShort);
        System.out.println("hi, my name is " + name);
        System.out.println(verdadeiro);
        System.out.println(falso);
        System.out.println(character);
    }
}
